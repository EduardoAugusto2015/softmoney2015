        double valor = 0.0;
        try {
            valor = Double.parseDouble(formatter.parse(tabelaDespesas.getValueAt(linhaSelecionada, 2).toString()).toString());
        } catch(Exception e) {
            e.printStackTrace();
        }

        descricaotext.setText(tabelaDespesas.getValueAt(linhaSelecionada, 1).toString());
