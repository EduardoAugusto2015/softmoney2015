     public void deletarLinha() {
        ModeloTabela model = (ModeloTabela) tabelaDespesas.getModel();
        int resposta = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir essa linha?", "Aviso", JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {

            Conexao delLin = new Conexao();
            delLin.abrirConexao();
            delLin.rs = null;
            try {
                delLin.st = delLin.con.createStatement();
                PreparedStatement st = delLin.con.prepareStatement("DELETE FROM despesas WHERE ID_despesas = ?");

                st.setInt(1, (int) model.getValueAt(tabelaDespesas.getSelectedRow(), 0));
                st.executeUpdate();
                preencherTabela();
                menu.atualizarSaldoAtual();
            } catch (SQLException | HeadlessException e) {
            }
        }
    }

    public void preencherTabela() {
        ArrayList dados = new ArrayList();

        String[] colunas = new String[]{"ID", "Descrição", "Valor", "Data", "Categoria"};

        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);

        Sessao sessao = Sessao.getInstancia();
        int id = sessao.getIdUsuario();

        Conexao tabela = new Conexao();
        tabela.abrirConexao();
        tabela.rs = null;

        try {
            tabela.st = tabela.con.createStatement();
            PreparedStatement st = tabela.con.prepareStatement("SELECT ID_despesas, descricao, valor, periodo, categoria FROM despesas WHERE id_usuario = ?");

            st.setInt(1, id);
            tabela.rs = st.executeQuery();
            
            while (tabela.rs.next()) {
                //String valor = tabela.rs.getString("valor");
                //valor = valor.replace('.', ',');
                //valor = "R$ " + valor;
                double valor = tabela.rs.getDouble("valor");
                dados.add(new Object[]{tabela.rs.getInt("ID_despesas"), tabela.rs.getString("descricao"), formatter.format(valor), tabela.rs.getString("periodo"), tabela.rs.getString("categoria")});
            }
        } catch (SQLException | HeadlessException e) {
        }

        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        modelo.addTableModelListener(new TableModelListener() {

            @Override
            public void tableChanged(TableModelEvent e) {
                int r = e.getFirstRow();
                int c = e.getColumn();
                System.out.println("Editado: " + r + "," + c);
            }

        });

        tabelaDespesas.setModel(modelo);
        tabelaDespesas.getColumnModel().getColumn(0).setPreferredWidth(162);
        tabelaDespesas.getColumnModel().getColumn(0).setResizable(false);
        tabelaDespesas.getColumnModel().getColumn(0).setCellRenderer(centralizado);
        tabelaDespesas.getColumnModel().getColumn(1).setPreferredWidth(162);
        tabelaDespesas.getColumnModel().getColumn(1).setResizable(false);
        tabelaDespesas.getColumnModel().getColumn(1).setCellRenderer(centralizado);
        tabelaDespesas.getColumnModel().getColumn(2).setPreferredWidth(163);
        tabelaDespesas.getColumnModel().getColumn(2).setResizable(false);
        tabelaDespesas.getColumnModel().getColumn(2).setCellRenderer(centralizado);
        tabelaDespesas.getColumnModel().getColumn(3).setPreferredWidth(162);
        tabelaDespesas.getColumnModel().getColumn(3).setResizable(false);
        tabelaDespesas.getColumnModel().getColumn(3).setCellRenderer(centralizado);
        tabelaDespesas.getColumnModel().getColumn(4).setPreferredWidth(161);
        tabelaDespesas.getColumnModel().getColumn(4).setResizable(false);
        tabelaDespesas.getColumnModel().getColumn(4).setCellRenderer(centralizado);
        tabelaDespesas.getTableHeader().setReorderingAllowed(false);
        tabelaDespesas.setAutoResizeMode(tabelaDespesas.AUTO_RESIZE_OFF);
        tabelaDespesas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        ((DefaultTableCellRenderer) tabelaDespesas.getTableHeader().getDefaultRenderer()).setHorizontalAlignment(SwingConstants.CENTER);
    }
